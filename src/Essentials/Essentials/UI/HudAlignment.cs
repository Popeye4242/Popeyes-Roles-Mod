namespace Essentials.UI
{
    /// <summary>
    /// Alignment positions respective to the game hud.
    /// </summary>
    public enum HudAlignment
    {
        /// <summary>
        /// Bottom left corner (default).
        /// </summary>
        BottomLeft,
        /// <summary>
        /// Bottom right corner.
        /// </summary>
        BottomRight,
        /// <summary>
        /// Top left corner.
        /// </summary>
        TopLeft,
        /// <summary>
        /// Top right corner.
        /// </summary>
        TopRight,
        /// <summary>
        /// Bottom vertically, centered horizontally.
        /// </summary>
        Bottom,
        /// <summary>
        /// Top vertically, centered horizontally.
        /// </summary>
        Top,
        /// <summary>
        /// Left horizontally, centered vertically.
        /// </summary>
        Left,
        /// <summary>
        /// Right horizontally, centered vertically.
        /// </summary>
        Right
    }
}